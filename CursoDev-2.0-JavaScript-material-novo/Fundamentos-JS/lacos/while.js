/* 
    A estrutura do while tem 2 partes:
        1. Condição: até quando o loop deve acontecer
        2. Bloco de codigo: oque será repetido

        while(condição) 
*/

let numero1 = 0
const numero2 = 10

while(numero1 <= numero2){
    console.log(`Incrementando a variável numero1: ${numero1}`);
    numero1++
}