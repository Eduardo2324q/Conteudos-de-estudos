// Operadores matemáticos

const numero1 = 2
const numero2 = 3

// Soma

const soma = numero1 + numero2
console.log(soma);

// subtração 

const subtracao = numero1 - numero2
console.log(subtracao);

//multiplcação

const multiplcacao = numero1 * numero2
console.log(multiplcacao);


//divisão
const divisao = numero1 / numero2
console.log(divisao);


// resto da divisão - %
const resto = numero1 % numero2
console.log(resto);

console.log(10 % 2 === 0); // True - significa se o numero é par

console.log(7 % 2 === 0); // False - significa se o numero é impar
